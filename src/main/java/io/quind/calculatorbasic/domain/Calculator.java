package io.quind.calculatorbasic.domain;

public class Calculator {

    public double addTwoNumbers(double num1, double num2){return num1 + num2;}
    public double subtractTwoNumbers(double num1, double num2){return num1 - num2;}
    public double divisionTwoNumbers(double num1, double num2) {return num1 / num2;}
    public double multiplicationTwoNumbers(double num1, double num2){return num1 * num2;}
}
